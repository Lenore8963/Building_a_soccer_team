package team;

/**
 * This enum represents the skill levels of players in a soccer team. Each skill level is
 * represented by a distinct value, from LEVEL_1 (the lowest skill level) to LEVEL_5 (the highest
 * skill level).
 */
public enum SkillLevel {
  LEVEL_1, LEVEL_2, LEVEL_3, LEVEL_4, LEVEL_5
}
